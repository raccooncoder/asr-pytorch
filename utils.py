def int_to_text(labels, idx2char, replace=True):
    string = ""
    for i in labels:
        try:
            string += idx2char[i][0]
        except:
            string += ''
        
    if replace:
        return ''.join(string).replace('', ' ')
    else:
        return ''.join(string)

def GreedyDecoder(output, labels, label_lengths, idx2char, blank_label=27, collapse_repeated=True, replace=True):
    arg_maxes = torch.argmax(output, dim=2)
    decodes = []
    targets = []
    
    for i, args in enumerate(arg_maxes):
        decode = []
        targets.append(int_to_text(labels[i][:label_lengths[i]].tolist(), idx2char, replace))
        for j, index in enumerate(args):
            if index != blank_label:
                if collapse_repeated and j != 0 and index == args[j -1]:
                    continue
                decode.append(index.item())
        decodes.append(int_to_text(decode, idx2char, replace))
    return decodes, targets